#include <bits/stdc++.h>
using namespace std;

int main() {
  // 一年の秒数
  int seconds = 365 * 24 * 60 * 60;

  // 以下のコメント/* */を消して追記する
  cout << 1*seconds/* 1年は何秒か */ << endl;
  cout << 2*seconds/* 2年は何秒か */ << endl;
  cout << 5*seconds/* 5年は何秒か */ << endl;
  cout << 10*seconds/* 10年は何秒か */ << endl;
  return 0;
}
